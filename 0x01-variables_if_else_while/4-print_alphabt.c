#include <stdio.h>
/**
 * main - Entry point
 *
 * Description: using the main function
 *
 * this program prints "Programming is positive, zero, or negative"
 * Return: Always 0 (success)
 */

int main(void)
{
	char c;

	c = 'a';
	while
		(c <= 'z') {
			if ((c != 'q' && c != 'e') && c <= 'z')
				putchar(c);
			c++;
		}
	putchar('\n');
	return (0);
}
